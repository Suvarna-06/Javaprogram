package ex_10_For_loop;

public class Lab074 {
    public static void main(String[] args) {
        // For Loop
        //        // Help you to repeat a block of code.
        //
        //        // Initialization -> Condition -> Updation(Incre/decre)
        //        // ICU

        for (int i= 0;i<10;i++){
            System.out.println();
            // 0,1,2......9 - times it got executed - 10 times ( 0 to 9)
        }



    }
}
