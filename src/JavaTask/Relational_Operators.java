package JavaTask;

public class Relational_Operators {
    public static void main(String[] args) {
        int y = 5;
        int a = 10;
        System.out.println("a == y: " + (a == y)); // Equals: false
        System.out.println("a != y: " + (a != y)); // Not Equals: true
        System.out.println("a > y: " + (a > y));  // Greater than: true
        System.out.println("a < y: " + (a < y));  // Less than: false
        System.out.println("a >= y: " + (a >= y)); // Greater than or equal to: true
        System.out.println("a <= y: " + (a <= y)); // Less than or equal to: false

    }
}
