package ex_07_Increment_Decrement_Operators;

public class Lab052_Advance_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a);
        // A + B
        // A -> a++ -> Exp1(A) -> 10 , a ->11
        // B -> a -> 11, Exp(B) ->11
        // A+B -> 10 + 11 -> 21

    }
}
